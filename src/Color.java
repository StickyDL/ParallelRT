/**
 * Very basic Color class to keep track of colors for
 * the RayTracers.
 *
 * @author  Steve Glazer
 * @author  Sara Jackson
 * @author  Sam Milton
 * @version 16-Feb-2012
 */
public class Color {

	double r;
	double g;
	double b;
	
	// Constructor given the rgb values
	public Color( double r, double g, double b){
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
}
